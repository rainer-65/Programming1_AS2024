package lecture01.examples;

public class Factorial {
    public static void main(String[] args) {
        int one = 1;
        System.out.println(one);

        int two = one * 2;
        System.out.println(two);

        int three = two * 3;
        System.out.println(three);

        int four = three * 4;
        System.out.println(four);

        int five = four * 5;
        System.out.println(five);

        int six = five * 6;
        System.out.println(six);

        int seven = six * 7;
        System.out.println(seven);

        int eight = seven * 8;
        System.out.println(eight);

        int nine = eight * 9;
        System.out.println(nine);

        int ten = nine * 10;
        System.out.println(ten);

        int eleven = ten * 11;
        System.out.println(eleven);

        int twelve = eleven * 12;
        System.out.println(twelve);

        int thirteen = twelve * 13;
        System.out.println(thirteen);

        int fourteen = thirteen * 14;
        System.out.println(fourteen);

        int fifteen = fourteen * 15;
        System.out.println(fifteen);

        int sixteen = fifteen * 16;
        System.out.println(sixteen);

        int seventeen = sixteen * 17;
        System.out.println(seventeen);

        int eighteen = seventeen * 18;
        System.out.println(eighteen);

        int nineteen = eighteen * 19;
        System.out.println(nineteen);

        int twenty = nineteen * 20;
        System.out.println(twenty);
    }
}
