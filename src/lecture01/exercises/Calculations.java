package lecture01.exercises;

public class Calculations {
    public static void main(String[] args) {
        int a = 13;
        int b = 6;

        a = a / b;
        System.out.println(a);

        a = a + b * 3;
        System.out.println(a);

        a = a / b - 3;
        System.out.println(a);
    }
}
